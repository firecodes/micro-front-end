export const RegExp = {
  // 年龄
  age: /^(?:[1-9][0-9]?|1[0-4][0-9]|150)$/,
  // 验证邮箱
  email: /^[\w]+(?:\.[\w]+)*@(?:[\w](?:[\w-]*[\w])?\.)+[\w](?:[\w-]*[\w])?/,
  // 验证带区号的联系方式 类似 +00 222222
  area_code_contact_type: /^(\+[0-9]*)\s([0-9]*)$/,
  // 正整数
  positive_integer: /^[1-9]\d*$/
}
